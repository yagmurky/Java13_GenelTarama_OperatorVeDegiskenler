package com.yagmur.operatorler;

public class Operatorler_Tanim {

    /**
     * Operatorler:
     * Operatörler, bir program içinde değerlerle yapılan işlemleri temsil eden semboller
     * veya özel kelimelerdir.
     *
     *
     Java'da kullanılan temel operatör türleri şunlardır:
     ----------------------------------------------------
     1-Aritmetik Operatörler: Matematiksel işlemleri gerçekleştirmek için kullanılır.
     + (Toplama)
     - (Çıkarma)
     * (Çarpma)
     / (Bölme)
     % (Mod alma)
     ---------------------------------------------------
     2- Karşılaştırma Operatörleri: Değerleri karşılaştırmak için kullanılır
     ve genellikle koşullu ifadelerde kullanılır.

     == (Eşit mi?)
     != (Eşit değil mi?)
     > (Büyük mü?)
     < (Küçük mü?)
     >= (Büyük veya eşit mi?)
     <= (Küçük veya eşit mi?)
     ---------------------------------------------------
     3- Mantıksal Operatörler: Mantıksal işlemleri gerçekleştirmek için kullanılır.

     && (VE - "and" operatörü)
     || (VEYA - "or" operatörü)
     ! (DEĞİL - "not" operatörü)
     ---------------------------------------------------

     4- Atama Operatörleri: Değişkenlere değer atamak için kullanılır.

     = (Atama)
     += (Topla ve ata)
     -= (Çıkar ve ata)
     *= (Çarp ve ata)
     /= (Böl ve ata)
     ---------------------------------------------------
     5- Artırma/Azaltma Operatörleri:

     ++ (Artırma)
     -- (Azaltma)

     */
}
